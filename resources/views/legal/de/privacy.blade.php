## Technische Zugriffsdaten (Server-Logs)

Beim Aufruf dieser Website können durch den Hosting-Anbieter automatisch technische Daten in sogenannten Server-Logfiles verarbeitet werden
(z. B. IP-Adresse, Zeitpunkt des Zugriffs, aufgerufene Seite, User-Agent, Referrer-URL).
Die Verarbeitung erfolgt zur Gewährleistung der Sicherheit und Stabilität der Website sowie zur Fehleranalyse.
Eine Auswertung zur persönlichen Identifikation findet nicht statt.

