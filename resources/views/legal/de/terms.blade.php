## 1. Geltungsbereich

Diese Nutzungsbedingungen gelten für die Nutzung der Plattform VHeart. Mit der Nutzung der Website, insbesondere dem Einreichen von Inhalten oder dem Bestätigen einer Nutzererlaubnis, erklärt sich der Nutzer mit diesen Bedingungen einverstanden.

Der Fokus liegt auf:
- Dem Einreichen von Videoclips (Link-Übermittlung).
- Der Bearbeitung, Verwertung und Veröffentlichung dieser Clips.
- Der Erstellung und Distribution von Clip-Compilations.
- Regelungen zur Zweitverwertung (z.B. Reactions).

## 2. Leistungsbeschreibung & Zweck

VHeart ist eine Community-Plattform zur Kuratierung von Livestream-Highlights.

- VHeart sammelt eingereichte Videoclips und erstellt daraus kuratierte Compilations.
- Diese Compilations werden auf dem Plattform eigenen Youtube-Kanal veröffentlicht.
- Das Projekt verfolgt ein spenden-orientiertes Konzept und soll Community-Inhalte mit einem guten Zweck verbinden.

**Wichtig**: Es besteht kein Rechtsanspruch auf die Veröffentlichung, Bearbeitung, Speicherung oder Archivierung eingereichter Clips.

## 3. Einräumung von Nutzungsrechten

Die Einreichung und Nutzung von Clips ist bei VHeart an eine explizite Zustimmung des jeweiligen Urhebers bzw. Streamers gebunden.

Ein Clip wird erst dann in das System aufgenommen und für Compilations verarbeitet, wenn der Urheber über das entsprechende Bestätigungsfeld (Opt-in) auf der Webseite sein Einverständnis erklärt hat.

Mit der Bestätigung gewährt der Urheber VHeart das einfache, unentgeltliche, zeitlich und örtlich unbeschränkte Recht, die freigegebenen Clips im Rahmen des VHeart-Projekts zu nutzen.

Dies umfasst insbesondere das Recht, die Clips:
- zu speichern,
- zu sichten und zu kuratieren,
- zu kürzen,
- zu bearbeiten,
- mit Effekten, Overlays, Untertiteln, Musik oder anderen Gestaltungselementen zu versehen,
- mit anderen Clips zu kombinieren,
- in Compilations einzubinden,
- öffentlich zugänglich zu machen,
- auf VHeart-eigenen Kanälen zu veröffentlichen,
- in Ausschnitten für Teaser, Shorts, Social-Media-Posts oder ähnliche projektbezogene Inhalte zu verwenden.

Die Veröffentlichung erfolgt insbesondere auf Youtube sowie gegebenenfalls auf weiteren VHeart-eigenen Social-Media-Kanälen.

## 4. Namensnennung & Verlinkung

VHeart nennt den jeweiligen Streamer sowie den Clipper des Clips nach Möglichkeit im Overlay, in der Videobeschreibung oder auf andere geeignete Weise

Der Streamer wird mit einem Link zu seinem angegebenen Kanal genannt. Auch der Clipper kann mit seinem angegebenen Namen oder Profil genannt werden.

Ein Anspruch auf eine bestimmte Art, Platzierung, Dauer oder Ausgestaltung der Nennung besteht nicht. VHeart bemüht sich jedoch um eine faire und transparente Zuordnung der Clips.

