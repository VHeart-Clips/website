- **Vor Veröffentlichung:** Ein Widerruf der Nutzungserlaubnis für die jeweils anstehende Compilation ist bis spätestens **Mittwoch um 0:00 Uhr** der jeweiligen Veröffentlichungs Woche möglich. Clips, deren Widerruf nach dieser Frist eingeht, können für die aktuelle Folge ggf. nicht mehr berücksichtigt werden, da die Produktion bereits abgeschlossen ist.
