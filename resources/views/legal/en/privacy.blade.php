## Third-party services and content

This website may use third-party services to provide fonts, embedded content, or external links.
If such services are loaded from third-party servers, your IP address and technical data may be transmitted to those providers.

### Google Fonts

We use Google Fonts for consistent typography. If fonts are loaded from Google servers,
Google may receive your IP address and technical request data. If you host the fonts locally,
no connection to Google is made for font delivery.

[Google Privacy Policy](https://policies.google.com/privacy)

### Discord

If we link to or embed Discord content (e.g. invite links or widgets), Discord may process technical data when you open the link or load the embed.
The scope of processing depends on your Discord settings and whether you are logged in.

### YouTube

If YouTube videos are embedded, Google/YouTube may process technical data (including your IP address) when the video is loaded.
If you are logged in, this data may be associated with your account.

[Google/YouTube Privacy Policy](https://policies.google.com/privacy)

### CATAAS (The Cat API / cataas.com)

If this website loads images from cataas.com, your browser will connect to that service to retrieve the image.
This may transmit your IP address and technical request data to cataas.com.

[Visit cataas.com](https://cataas.com)

### Twitch

